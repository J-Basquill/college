public class Lab023
{
	public static void main(String[] args)
	{
	int numberX;						//Declaration of variable numberX
	numberX = 10;						//Assigning 10 to numberX
	
	int numberY;						//Declaration of variable numberY
	numberY = 43;						//Assigning 43 to numberY
	
	int numberZ;						//Declaration of variable numberZ
	numberZ = 15;						//Assigning 15 to numberZ
	
	int sum;							//Declaration of variable sum
	sum = numberX+numberY+numberZ; 		//Assigning sum of variables to sum
	
	int product;						//Declaration of variable product	
	product = numberX*numberY*numberZ;	//Assigning product of variables to product
	
	System.out.println("The sum of the variables is equal to "+sum);
	System.out.println("The product of the variables is equal to "+product);
	
	}
	
	
}