public class Test6
{
	public static void main(String[] args)
	{
		int num1 =20;				//Declare num1 = 20
		int num2 =10;				//Declare num2 = 10
		
		num1+=num2;					//Declare num1 = num1 + num2 = 30
		System.out.println(num1);	
		
		num1-=num2;					//Declare num1 = num1 - num2 = 20
		System.out.println(num1);
		
		num1/=num2;					//Declare num1 = num1 / num2 = 2
		System.out.println(num1);
		
		num1*=num2;					//Declare num1 = num1 * num2 = 20
		System.out.println(num1);
		
	}
}	